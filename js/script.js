window.onload = function getaHaiku(){

	datadehoje();

	var hoje = new Date();

	var gh = hoje.getDate();

	var haikai = listahaikai[gh]; 
	
	document.getElementById('h').innerHTML = haikai;

};


function datadehoje() {

	var today = new Date();

	var year = today.getFullYear();

	var day = today.getDate();

	var diasdasemana = ['Domingo', 'Segunda-Feira','Terça-Feira','Quarta-Feira',
	'Quinta-Feira', 'Sexta-Feira', 'Sábado'];

	weekday = diasdasemana[today.getDay()];

	var mesPtBr = ['Janeiro', 'Fevereiro', 'Março', 'Abril', 'Maio','Junho','Julho', 'Agosto',
	'Setembro','Outubro','Novembro','Dezembro']; 

	month = mesPtBr[today.getMonth()];
	
	document.getElementById('dataatual').innerHTML = (weekday+', '+day+' de '+month+' de '+year);	
	
}


